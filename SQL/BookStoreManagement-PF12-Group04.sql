DROP DATABASE IF EXISTS bookstore_management_system;
CREATE DATABASE bookstore_management_system
    CHARACTER SET utf8;
USE bookstore_management_system;
CREATE TABLE categories
(
    CategoryID   INT UNSIGNED AUTO_INCREMENT
        PRIMARY KEY,
    CategoryName VARCHAR(255)
);

CREATE TABLE publishers
(
    PublisherID   INT UNSIGNED AUTO_INCREMENT
        PRIMARY KEY,
    PublisherName VARCHAR(255)
);

CREATE TABLE authors
(
    AuthorID   INT UNSIGNED AUTO_INCREMENT
        PRIMARY KEY,
    AuthorName VARCHAR(255)
);

CREATE TABLE languages
(
    LanguageID   SMALLINT UNSIGNED AUTO_INCREMENT
        PRIMARY KEY,
    LanguageName VARCHAR(50)
);

CREATE TABLE books
(
    BookID          INT UNSIGNED AUTO_INCREMENT
        PRIMARY KEY,
    Title           VARCHAR(255) NOT NULL,
    Price           INT UNSIGNED,
    Format          TINYINT,
    ISBN13          CHAR(13),
    ISBN10          CHAR(10),
    PublisherID     INT UNSIGNED,
    PublicationDate DATE,
    NumberOfPages   INT UNSIGNED,
    Dimensions      VARCHAR(50),
    Description     VARCHAR(255),
    Quantity        INT UNSIGNED,
#     Hang co san, Sap ve, Het hang, Ngung kinh doanh
    Status          TINYINT DEFAULT 1,
    UNIQUE (ISBN10),
    UNIQUE (ISBN13),
    FOREIGN KEY (PublisherID) REFERENCES publishers (PublisherID)
);

CREATE TABLE categories_books
(
    CategoryID INT UNSIGNED NOT NULL,
    BookID     INT UNSIGNED NOT NULL,
    PRIMARY KEY (CategoryID, BookID),
    FOREIGN KEY (BookID) REFERENCES books (BookID),
    FOREIGN KEY (CategoryID) REFERENCES categories (CategoryID)
);

CREATE TABLE authors_books
(
    AuthorID INT UNSIGNED NOT NULL,
    BookID   INT UNSIGNED NOT NULL,
    PRIMARY KEY (AuthorID, BookID),
    FOREIGN KEY (BookID) REFERENCES books (BookID),
    FOREIGN KEY (AuthorID) REFERENCES authors (AuthorID)
);
CREATE TABLE languages_books
(
    LanguageID SMALLINT UNSIGNED NOT NULL,
    BookID     INT UNSIGNED      NOT NULL,
    PRIMARY KEY (LanguageID, BookID),
    FOREIGN KEY (BookID) REFERENCES books (BookID),
    FOREIGN KEY (LanguageID) REFERENCES languages (LanguageID)
);

CREATE TABLE customers
(
    CustomerID   INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    FirstName    VARCHAR(50) NOT NULL,
    LastName     VARCHAR(255),
    ContactTitle VARCHAR(255),
    Gender       TINYINT,
    BirthDate    DATE,
    Address      VARCHAR(255),
    City         VARCHAR(50),
    Phone        VARCHAR(20) UNIQUE,
    Fax          VARCHAR(20),
    Email        VARCHAR(255),
    Note         VARCHAR(255)
);

CREATE TABLE employees
(
    EmployeeID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    FirstName  VARCHAR(50) NOT NULL,
    LastName   VARCHAR(255),
    JobTitle   VARCHAR(255),
    Gender     TINYINT,
    BirthDate  DATE,
    HireDate   DATE,
    Address    VARCHAR(255),
    City       VARCHAR(50),
    Phone      VARCHAR(20) UNIQUE,
    Fax        VARCHAR(20),
    Email      VARCHAR(255),
    Note       VARCHAR(255),
    UserName   VARCHAR(100),
    Password   VARCHAR(50)
);

CREATE TABLE invoices
(
    InvoiceID   INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    CustomerID  INT UNSIGNED,
    EmployeeID  INT UNSIGNED,
    CreatedTime DATETIME DEFAULT NOW(),
    Description VARCHAR(255),
    FOREIGN KEY (CustomerID) REFERENCES customers (CustomerID),
    FOREIGN KEY (EmployeeID) REFERENCES employees (EmployeeID)
);

CREATE TABLE invoices_details
(
    InvoiceID    INT UNSIGNED,
    BookID       INT UNSIGNED,
    ItemQuantity INT UNSIGNED,
    Amount       INT UNSIGNED,
    PRIMARY KEY (InvoiceID, BookID),
    FOREIGN KEY (InvoiceID) REFERENCES invoices (InvoiceID),
    FOREIGN KEY (BookID) REFERENCES books (BookID)
);

